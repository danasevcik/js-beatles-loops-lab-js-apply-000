function theBeatlesPlay(musArr, instArr) {
  var ansArray = [];
  
  for (var i = 0; i < musArr.length; i++) {
    var musEle = musArr[i];
    var instEle = instArr[i];
    var musInstStr = musEle + ' plays ' + instEle;
    
    ansArray.push(musInstStr);
  }
  return ansArray;
}

